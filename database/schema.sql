USE allyApp;

CREATE TABLE books(
  id INT(10) NOT NULL AUTO_INCREMENT PRIMARY KEY,
  title VARCHAR(350) NOT NULL,
  author VARCHAR(250),
  genre VARCHAR(250),
  link1 VARCHAR(2000),
  link2 VARCHAR(2000),
  link3 VARCHAR(2000)
);
  
CREATE TABLE childrensBooks(
  id INT(10) NOT NULL AUTO_INCREMENT PRIMARY KEY,
  title VARCHAR(350) NOT NULL,
  author VARCHAR(250),
  age_range VARCHAR(250),
  link1 VARCHAR(2000),
  link2 VARCHAR(2000),
  link3 VARCHAR(2000)
);
  
CREATE TABLE podcasts(
  id INT(10) NOT NULL AUTO_INCREMENT PRIMARY KEY,
  title VARCHAR(350) NOT NULL,
  link1 VARCHAR(2000),
  link2 VARCHAR(2000)
);
  
CREATE TABLE articles(
  id INT(10) NOT NULL AUTO_INCREMENT PRIMARY KEY,
  title VARCHAR(350) NOT NULL,
  author VARCHAR(250),
  link VARCHAR(2000),
  link2 VARCHAR(2000)
);
  
CREATE TABLE toWatch(
  id INT(10) NOT NULL AUTO_INCREMENT PRIMARY KEY,
  title VARCHAR(350) NOT NULL,
  source VARCHAR(150),
  link VARCHAR(2000),
  link2 VARCHAR(2000)
);